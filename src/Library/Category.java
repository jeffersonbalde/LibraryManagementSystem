    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        String name = inputname.getText();
        String status =  inputstatus.getSelectedItem().toString();
        
        try{
            preparedStatement = connection.prepareStatement("INSERT INTO Category(name, status) VALUES (?,?)");
            preparedStatement.setString(1, name);
            preparedStatement.setString(2, status);
            int k = preparedStatement.executeUpdate();
            
            if(k == 1){
                JOptionPane.showMessageDialog(this, "Category Created");
                inputname.setText("");
                inputstatus.setSelectedIndex(-1);
                inputname.requestFocus();
                
                CategoryLoad();
            }else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        }catch(SQLException ex){
            Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addbtnActionPerformed

