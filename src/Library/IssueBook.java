    private void inputidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inputidKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String mid = inputid.getText();
            
            try {
                preparedStatement = connection.prepareStatement("SELECT * FROM member WHERE id = ? ");
                preparedStatement.setString(1, mid);
                resultSet = preparedStatement.executeQuery();
                
                if(resultSet.next() == false){
                    JOptionPane.showMessageDialog(this, "MEMBER ID NOT FOUND");
                }else {
                    String memberName = resultSet.getString("name");
                    inputname.setText(memberName.trim());
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_inputidKeyPressed

