    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        String mid = inputid.getText();
        BookItem  bitem = (BookItem) inputbook.getSelectedItem();
        
        SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        String issuedate = date_format.format(inputdate.getDate());
        
        SimpleDateFormat date_format1 = new SimpleDateFormat("yyyy-MM-dd");
        String returndate = date_format.format(inputreturndate.getDate());
        
        try{
            preparedStatement = connection.prepareStatement("INSERT INTO issuebook(name, book, date, returndate) VALUES (?,?,?,?)");
            preparedStatement.setString(1, mid);
            preparedStatement.setInt(2, bitem.id);
            preparedStatement.setString(3, issuedate);
            preparedStatement.setString(4, returndate);
            int k = preparedStatement.executeUpdate();
            
            if(k == 1){
                JOptionPane.showMessageDialog(this, "Book Issued");
                
                inputid.setText("");
                inputname.setText("");
                inputbook.setSelectedIndex(-1);
                 
                IssueBookLoad();
            }else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        }catch(SQLException ex){
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addbtnActionPerformed

